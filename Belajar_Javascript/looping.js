// FOR
for(let i = 1; i <=10; i++ ){
    console.log("Saya Ada Di Dalam Loop")
    console.log(i);
}
for(let i = 0; i <=20; i+=2 ){
    console.log(i);
}
// (-)
for(let i = 20; i >=0; i-- ){
    console.log(i);
}
// (*)
for(let i = 10; i <=1000; i*=10 ){
    console.log(i);
}

for(let i = 10; i <=100; i**1 ){
    console.log(i);
}
